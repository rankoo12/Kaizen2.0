// ─── Healing ─────────────────────────────────────────────────────────────────

export type FailureClass =
  | 'ELEMENT_REMOVED'
  | 'ELEMENT_MUTATED'
  | 'ELEMENT_OBSCURED'
  | 'PAGE_NOT_LOADED'
  | 'TIMING'
  | 'LOGIC_FAILURE';

export type ClassifiedFailure = {
  stepResult: StepExecutionResult;
  /** DB id of the step_results row, if one was created for this execution. */
  stepResultId?: string;
  failureClass: FailureClass;
  step: StepAST;
  previousSelector: string;
};

export type HealingContext = {
  tenantId: string;
  runId: string;
  /** Typed as unknown here; consuming modules import Page from playwright directly. */
  page: unknown;
};

export type HealingAttempt = {
  succeeded: boolean;
  newSelector: string | null;
  durationMs: number;
};

export type HealingResult = {
  succeeded: boolean;
  strategyUsed: string;
  newSelector: string | null;
  attempts: number;
  durationMs: number;
};

// ─── LLM Gateway ─────────────────────────────────────────────────────────────

export type LLMResolutionResult = {
  selectors: SelectorEntry[];
  /** True if this response was served from the prompt hash cache — no tokens consumed. */
  fromCache: boolean;
  promptTokens: number;
  completionTokens: number;
  templateVersion: string;
  /** The kaizenId string the LLM returned, before we map it to selectors. */
  llmPickedKaizenId?: string | null;
  /** The exact ranked candidates that were shown to the LLM in the prompt. */
  llmPromptedCandidates?: CompactCandidate[];
};

